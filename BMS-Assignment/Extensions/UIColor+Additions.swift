import UIKit

extension UIColor {
    
    static var AppDarkBlackColor: UIColor {
        return UIColor(named: "AppDarkBlackColor") ?? UIColor.red
    }
    
    static var AppLightBlackColor: UIColor {
        return UIColor(named: "AppLightBlackColor") ?? UIColor.red
    }
    
    static var AppYelloColor: UIColor {
        return UIColor(named: "AppYelloColor") ?? UIColor.red
    }
    
    static var AppGreenColor: UIColor {
        return UIColor(named: "AppGreenColor") ?? UIColor.red
    }
    
    static var AppLightBlackTextColor: UIColor {
        return UIColor(named: "AppLightBlackTextColor") ?? UIColor.red
    }
}
